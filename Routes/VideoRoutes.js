const express=require('express')
const { getLikedVideos, getVideo, getSavedVideos, getWatchedVideos, addVideo, likeVideo, addComment, updateVideo, getVideoDetail, deleteVideo, saveVideo, getAllVideos, replyToComment, getSearchResults, getMyVideos } = require('../Controllers/VideoController')
