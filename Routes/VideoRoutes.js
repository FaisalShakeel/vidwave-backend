const express=require('express')
const { getLikedVideos, getSavedVideos, getWatchedVideos, addVideo, likeVideo, addComment, updateVideo, getVideoDetail, deleteVideo, saveVideo, getAllVideos, replyToComment, getSearchResults, getMyVideos, deleteWatchHistory, getVideoDetails } = require('../Controllers/VideoController')
const verifyUser = require('../Middlewares/VerifyUser')
const videoRouter=express.Router()
videoRouter.get("/all-videos",getAllVideos )
videoRouter.get("/video-details/:videoId",getVideoDetails )
videoRouter.get("/my-videos",verifyUser,getMyVideos)
videoRouter.delete("/delete-video/:videoId",verifyUser,deleteVideo)
videoRouter.put("/update-video",verifyUser,updateVideo)
videoRouter.get("/liked-videos",verifyUser, getLikedVideos)
videoRouter.get("/saved-videos",verifyUser,getSavedVideos )
videoRouter.get("/watched-videos",verifyUser, getWatchedVideos )
videoRouter.delete("/delete-watch-history",verifyUser,deleteWatchHistory)
videoRouter.post("/upload-video",verifyUser,addVideo )
videoRouter.post("/like-video",verifyUser, likeVideo )
videoRouter.post("/add-comment/",verifyUser,addComment )
videoRouter.post("/reply-to-comment",verifyUser,replyToComment)
videoRouter.post("/search",getSearchResults)
videoRouter.post("/updatevideo/:ID",updateVideo)

module.exports=videoRouter